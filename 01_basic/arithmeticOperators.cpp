#include<iostream>
using namespace std;
int main(){
    int x = 8;
    int y = 5;
    cout<<x+y<<endl;
    cout<<x-y<<endl;
    cout<<x/y<<endl;
    cout<<x*y<<endl;    
    cout<<x%y<<endl; 
    
    //increment and decrement

    int n = 9;
    cout<<n<<endl;
    n++;    //post increment
    cout<<n<<endl;

    ++n ;    //pre increment
    cout<<n<<endl;

}